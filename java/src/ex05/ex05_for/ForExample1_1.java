package ex05.ex05_for;

public class ForExample1_1 {

    public static void main(String[] args) {
        int cnt;
        for (cnt = 1; cnt <= 10; cnt++) {
            System.out.println(cnt);
        }
        System.out.println(cnt);
        System.out.println("끝");

        for (cnt = 10; cnt >= 1; cnt--) {
            System.out.println(cnt);
        }
    }
}
